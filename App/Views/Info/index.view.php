        <p class="grey">Dôležité ustanovenia</p>
        <ul>
            <li>základová platňa pripravená k montáži musí byť v tolerancii ± 5 mm od vodorovného stavu a musia byť dodržané všetky pravé uhly</li>
            <li>pred začatím montáže musí byť základ odizolovaný</li>
            <li>najneskôr do jedného mesiaca od ukončenia montáže musí byť prevedený minimálne dvojvrstvový náter všetkých exteriérových prvkov stavby aby sa zabránilo poškodeniu dreva vplyvom poveternostných podmienok</li>
